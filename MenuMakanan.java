    private void minusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_minusActionPerformed
        // TODO add your handling code here:
        int a = Integer.parseInt(jumlahtransaksi.getText());

        int b = a-1;
        jumlahtransaksi.setText(Integer.toString(b));
        if (a<=1){
            jumlahtransaksi.setText("1");
            JOptionPane.showMessageDialog(null, "Pesanan tidak dapat dilakukan", "ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_minusActionPerformed

