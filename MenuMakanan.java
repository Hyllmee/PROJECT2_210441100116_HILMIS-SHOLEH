    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        // TODO add your handling code here:
        String menu = null;
        int harga = 0;
        int hitung = 0;
        int jumlah = Integer.parseInt(jumlahtransaksi.getText());

        if (Soto.isSelected()) {
            menu = "Nasi Soto";
            harga = 50000;
            hitung = jumlah * harga;
        }
        else if (Campur.isSelected()) {
            menu = "Nasi Campur";
            harga = 15000;
            hitung = jumlah * harga;
        }
        else if (Rawon.isSelected()) {
            menu = "Nasi Rawon";
            harga = 15000;
            hitung = jumlah * harga;
        }
        else if (bebek.isSelected()) {
            menu = "Bebek Goreng";
            harga = 15000;
            hitung = jumlah * harga;
        }
        else if (Lele.isSelected()) {
            menu = "Pecel Lele";
            harga = 15000;
            hitung = jumlah * harga;
        }
        else if (Ayam.isSelected()) {
            menu = "Ayam Goreng";
            harga = 15000;
            hitung = jumlah * harga;
        }
        else if (Bihun.isSelected()) {
            menu = "Bihun";
            harga = 10000;
            hitung = jumlah * harga;
        }
        else if (Padang.isSelected()) {
            menu = "Nasi Padang";
            harga = 15000;
            hitung = jumlah * harga;
        }
        else if (Lalapan.isSelected()) {
            menu = "Lalapan Tempe Tahu";
            harga = 10000;
            hitung = jumlah * harga;
        }

        isipesanan.setText(""+ menu);
        isihargasatuan.setText(""+ harga);
        isijumlah.setText(""+ jumlah);
        isitotalbayar.setText("" + hitung);
        JOptionPane.showMessageDialog(null, "Pesanan berhasil", "Success!",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_okActionPerformed

